import UIKit

struct Password {
    let min: Int
    let max: Int
    let char: Character
    let password: String
}

extension Password {
    func isValid() -> Bool {
        let chars = Array(password)
        let count = chars.filter { $0 == char }
            .count
        return min <= count && count <= max
    }

    func isValidForNewRule() -> Bool {
        let chars = Array(password)

        var valid = false
        if chars.count >= min {
            valid = (chars[min - 1] == char)
        }

        if  chars.count >= max {
            valid = valid != (chars[max - 1] == char)
        }

        return valid
    }
}

